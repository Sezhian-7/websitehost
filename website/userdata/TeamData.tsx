const generateUniqueRandomNumber = () => {
  var arr = [];
  while (arr.length < 23) {
    var r = Math.floor(Math.random() * profileList.length) + 1;
    if (arr.indexOf(profileList[r]) === -1) arr.push(profileList[r]);
  }
  return arr;
};

export const dynamicProfile = () => {
  const arrayList = generateUniqueRandomNumber();
  let count = 0;
  for (let i = 0; i < dyanmicBubbles.length; i++) {
    if (dyanmicBubbles[i].i) {
      dyanmicBubbles[i].i = arrayList[count] || Staff1;

      count += 1;
    }
  }
  return [...dyanmicBubbles];
};
